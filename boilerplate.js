// Código boilerplate é um termo que se refere a blocos de código reutilizáveis que são usados em vários projetos ou contextos de programação. 
// Eles representam códigos padrões ou estruturas básicas que são comumente exigidas em muitas situações, servindo como ponto de partida para novos projetos.

// No contexto de programação, o termo boilerplate é derivado do jargão jornalístico e editorial, onde se referia a materiais ou textos que eram frequentemente reutilizados e que não precisavam ser reescritos a cada vez. 
// Da mesma forma, em programação, boilerplate são trechos de código que são frequentemente utilizados sem grandes mudanças, apenas adaptados conforme necessário.

// Exemplos de código boilerplate:

// 1. Estrutura básica de um arquivo JavaScript:
(function() {
  // Aqui vai o código do programa
})();

// 2. Estrutura de um arquivo HTML:
// (Este código não é JavaScript, mas apenas um exemplo para ilustrar o conceito de boilerplate)
// <!DOCTYPE html>
// <html lang="pt-br">
//   <head>
//     <meta charset="UTF-8">
//     <meta name="viewport" content="width=device-width, initial-scale=1.0">
//     <title>Meu Projeto</title>
//   </head>
//   <body>
//     <h1>Bem-vindo ao meu site</h1>
//   </body>
// </html>

// 3. Estrutura básica de um arquivo CSS:
// (Este código não é JavaScript, mas apenas um exemplo para ilustrar o conceito de boilerplate)
// * {
//   margin: 0;
//   padding: 0;
//   box-sizing: border-box;
// }

// Características do código boilerplate:
// - Reutilização: Serve como uma base que pode ser reutilizada em vários projetos.
// - Praticidade: Facilita o desenvolvimento, pois evita que você precise escrever o mesmo código várias vezes.
// - Padrão: Normalmente segue práticas ou convenções estabelecidas para facilitar a leitura e a manutenção.

// Vantagens:
// - Economia de tempo: Não é necessário escrever a mesma estrutura básica toda vez.
// - Facilidade: Ajuda a manter a consistência em projetos diferentes.

// Desvantagens:
// - Excesso de código: Pode tornar o código maior do que o necessário, dependendo da situação, especialmente se não for adaptado corretamente.

// Portanto, código boilerplate é basicamente uma forma de acelerar o desenvolvimento, utilizando estruturas prontas e padronizadas.
